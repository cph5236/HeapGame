/**
 * gen-heap-defs.mjs
 *
 * Scans src/svgs/, reads each SVG's viewBox, scales dimensions so the longest
 * side = TARGET_PX (preserving aspect ratio), and writes:
 *   src/data/heapItemDefs.ts  — the ObjectDef record used by the game
 *
 * Run: node scripts/gen-heap-defs.mjs
 */

import { readFileSync, writeFileSync, readdirSync } from 'fs';
import { join, basename, extname, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname  = dirname(fileURLToPath(import.meta.url));
const SVG_DIR    = join(__dirname, '..', 'src', 'svgs');
const OUT_DEFS   = join(__dirname, '..', 'src', 'data', 'heapItemDefs.ts');
const OUT_URLS   = join(__dirname, '..', 'src', 'data', 'heapSvgUrls.ts');

/** Longest side target in game pixels. Adjust to taste. */
const TARGET_PX = 96;

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

/** Convert a filename (no ext) into a clean camelCase-friendly slug. */
function slugify(name) {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')   // non-alphanumeric → dash
    .replace(/^-+|-+$/g, '')        // trim leading/trailing dashes
    .replace(/-+/g, '-');           // collapse runs
}

/**
 * Extract width & height from an SVG string.
 * Priority: viewBox → width/height attributes → fallback 100×100.
 */
function parseDimensions(svgText) {
  // 1. viewBox="minX minY W H"
  const vb = svgText.match(/viewBox\s*=\s*["']([^"']+)["']/i);
  if (vb) {
    const parts = vb[1].trim().split(/[\s,]+/);
    if (parts.length >= 4) {
      const w = parseFloat(parts[2]);
      const h = parseFloat(parts[3]);
      if (w > 0 && h > 0) return { w, h };
    }
  }

  // 2. width="W" height="H" (may have px/mm units — strip them)
  const wAttr = svgText.match(/\bwidth\s*=\s*["']([0-9.]+)/i);
  const hAttr = svgText.match(/\bheight\s*=\s*["']([0-9.]+)/i);
  if (wAttr && hAttr) {
    const w = parseFloat(wAttr[1]);
    const h = parseFloat(hAttr[1]);
    if (w > 0 && h > 0) return { w, h };
  }

  console.warn('  ⚠ Could not parse dimensions, using 100×100 fallback');
  return { w: 100, h: 100 };
}

/** Scale so longest side = target, preserving aspect ratio. Round to int. */
function scaleTo(w, h, target) {
  const scale = target / Math.max(w, h);
  return {
    width:  Math.max(1, Math.round(w * scale)),
    height: Math.max(1, Math.round(h * scale)),
  };
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

const files = readdirSync(SVG_DIR)
  .filter(f => extname(f).toLowerCase() === '.svg')
  .sort();

console.log(`Found ${files.length} SVGs in ${SVG_DIR}\n`);

const entries = files.map((filename, index) => {
  const svgPath = join(SVG_DIR, filename);
  const svgText = readFileSync(svgPath, 'utf8');
  const nameNoExt = basename(filename, extname(filename));
  const textureKey = slugify(nameNoExt);
  const { w, h } = parseDimensions(svgText);
  const { width, height } = scaleTo(w, h, TARGET_PX);

  console.log(`  [${String(index).padStart(2)}] ${filename}`);
  console.log(`       viewBox: ${w.toFixed(1)} × ${h.toFixed(1)}  →  game: ${width} × ${height}px`);
  console.log(`       key: "${textureKey}"`);

  return { keyid: index, textureKey, filename, width, height };
});

// ---------------------------------------------------------------------------
// Emit TypeScript
// ---------------------------------------------------------------------------

const lines = [
  `// AUTO-GENERATED by scripts/gen-heap-defs.mjs — do not edit by hand.`,
  `// Re-run the script after adding or removing SVGs.`,
  ``,
  `import { ObjectDef } from './heapTypes';`,
  ``,
  `/**`,
  ` * Maps keyid → object definition.`,
  ` * textureKey must match the key used in BootScene when loading the SVG.`,
  ` * width/height are scaled game-pixels (longest side = ${TARGET_PX}px).`,
  ` */`,
  `export const OBJECT_DEFS: Record<number, ObjectDef> = {`,
];

for (const e of entries) {
  lines.push(
    `  ${e.keyid}: { textureKey: '${e.textureKey}', filename: '${e.filename.replace(/'/g, "\\'")}', width: ${e.width}, height: ${e.height} }, // ${e.filename}`,
  );
}

lines.push(`};`);
lines.push(``);
lines.push(`/** Convenience array ordered by keyid. */`);
lines.push(`export const OBJECT_DEF_LIST = Object.values(OBJECT_DEFS);`);
lines.push(``);
lines.push(`export const HEAP_ITEM_COUNT = ${entries.length};`);
lines.push(``);

writeFileSync(OUT_DEFS, lines.join('\n'), 'utf8');
console.log(`\n✅ Wrote ${entries.length} entries to ${OUT_DEFS}`);

// ---------------------------------------------------------------------------
// Emit heapSvgUrls.ts — explicit ?url imports, reliable across all Vite versions
// ---------------------------------------------------------------------------

const urlLines = [
  `// AUTO-GENERATED by scripts/gen-heap-defs.mjs — do not edit by hand.`,
  `// Re-run the script after adding or removing SVGs.`,
  ``,
  `// Vite resolves each ?url import to the correct asset path in dev and production.`,
  `// Explicit per-file imports avoid glob key-normalisation issues with spaces in filenames.`,
  ``,
];

for (const e of entries) {
  const safeVar = `url_${e.keyid}`;
  urlLines.push(`import ${safeVar} from '../svgs/${e.filename.replace(/'/g, "\\'")}?url';`);
}

urlLines.push(``);
urlLines.push(`/** Maps textureKey → resolved asset URL. Used by BootScene to load SVGs. */`);
urlLines.push(`export const HEAP_SVG_URLS: Record<string, string> = {`);
for (const e of entries) {
  urlLines.push(`  '${e.textureKey}': url_${e.keyid},`);
}
urlLines.push(`};`);
urlLines.push(``);

writeFileSync(OUT_URLS, urlLines.join('\n'), 'utf8');
console.log(`✅ Wrote URL map to ${OUT_URLS}`);
