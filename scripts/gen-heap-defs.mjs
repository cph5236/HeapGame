/**
 * gen-heap-defs.mjs
 *
 * Scans src/sprites/Heap_sprites/<folder>/ for PNG sprites, reads their
 * dimensions, and writes:
 *   src/data/heapItemDefs.ts  — the ObjectDef record used by the game
 *   src/data/heapPngUrls.ts   — Vite ?url imports for each sprite PNG
 *
 * Folder rarity (0→1) is configured in scripts/sprite-config.mjs.
 * Folders not listed there are skipped.
 *
 * Run: node scripts/gen-heap-defs.mjs
 * Re-run after adding or removing sprites.
 */

import sharp from 'sharp';
import { writeFileSync, readdirSync } from 'fs';
import { join, basename, extname, dirname } from 'path';
import { fileURLToPath } from 'url';
import { FOLDER_RARITY, SPRITES_SUBDIR } from './sprite-config.mjs';

const __dirname   = dirname(fileURLToPath(import.meta.url));
const SPRITES_DIR = join(__dirname, '..', 'src', 'sprites', SPRITES_SUBDIR);
const OUT_DEFS    = join(__dirname, '..', 'src', 'data', 'heapItemDefs.ts');
const OUT_URLS    = join(__dirname, '..', 'src', 'data', 'heapPngUrls.ts');

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

/** Convert a filename (no ext) into a clean slug. */
function slugify(name) {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .replace(/-+/g, '-');
}

// ---------------------------------------------------------------------------
// Collect sprites, grouped by folder
// ---------------------------------------------------------------------------

const folders = Object.keys(FOLDER_RARITY).filter(f => FOLDER_RARITY[f] > 0);

const allEntries = [];

for (const folder of folders) {
  const folderPath = join(SPRITES_DIR, folder);
  let files;
  try {
    files = readdirSync(folderPath)
      .filter(f => extname(f).toLowerCase() === '.png')
      .sort();
  } catch {
    console.warn(`  ⚠ Folder not found, skipping: ${folderPath}`);
    continue;
  }

  const rarity = FOLDER_RARITY[folder];
  console.log(`\n📁 ${folder}  (rarity: ${rarity}, ${files.length} sprites)`);

  for (const filename of files) {
    const pngPath = join(folderPath, filename);
    const meta = await sharp(pngPath).metadata();
    const nameNoExt = basename(filename, extname(filename));
    const textureKey = slugify(nameNoExt);
    const { width, height } = meta;
    const keyid = allEntries.length;
    // Path relative to src/sprites/ — used in ?url imports from src/data/
    const relPath = `${SPRITES_SUBDIR}/${folder}/${filename}`;

    console.log(`  [${String(keyid).padStart(3)}] ${filename}  (${width}×${height}px)  key: "${textureKey}"`);

    allEntries.push({ keyid, textureKey, filename, relPath, folder, rarity, width, height });
  }
}

console.log(`\nTotal: ${allEntries.length} sprites across ${folders.length} folders\n`);

// ---------------------------------------------------------------------------
// Emit heapItemDefs.ts
// ---------------------------------------------------------------------------

const lines = [
  `// AUTO-GENERATED by scripts/gen-heap-defs.mjs — do not edit by hand.`,
  `// Re-run the script after adding or removing sprites from src/sprites/Heap_sprites/.`,
  ``,
  `import { ObjectDef } from './heapTypes';`,
  ``,
  `/**`,
  ` * Maps keyid → object definition.`,
  ` * textureKey must match the key used in BootScene when loading the PNG.`,
  ` * width/height are the actual PNG dimensions in game pixels.`,
  ` * rarity is 0→1 — controls spawn weight relative to other folders.`,
  ` */`,
  `export const OBJECT_DEFS: Record<number, ObjectDef> = {`,
];

for (const e of allEntries) {
  lines.push(
    `  ${e.keyid}: { textureKey: '${e.textureKey}', filename: '${e.relPath.replace(/'/g, "\\'")}', width: ${e.width}, height: ${e.height}, rarity: ${e.rarity} }, // ${e.folder}/${e.filename}`,
  );
}

lines.push(`};`);
lines.push(``);
lines.push(`/** Convenience array ordered by keyid. */`);
lines.push(`export const OBJECT_DEF_LIST = Object.values(OBJECT_DEFS);`);
lines.push(``);
lines.push(`export const HEAP_ITEM_COUNT = ${allEntries.length};`);
lines.push(``);

writeFileSync(OUT_DEFS, lines.join('\n'), 'utf8');
console.log(`✅ Wrote ${allEntries.length} entries to ${OUT_DEFS}`);

// ---------------------------------------------------------------------------
// Emit heapPngUrls.ts
// ---------------------------------------------------------------------------

const urlLines = [
  `// AUTO-GENERATED by scripts/gen-heap-defs.mjs — do not edit by hand.`,
  `// Re-run the script after adding or removing sprites from src/sprites/Heap_sprites/.`,
  ``,
  `// Vite resolves each ?url import to the correct asset path in dev and production.`,
  ``,
];

for (const e of allEntries) {
  const safeVar = `url_${e.keyid}`;
  urlLines.push(`import ${safeVar} from '../sprites/${e.relPath.replace(/'/g, "\\'")}?url';`);
}

urlLines.push(``);
urlLines.push(`/** Maps textureKey → resolved asset URL. Used by BootScene to load sprites. */`);
urlLines.push(`export const HEAP_PNG_URLS: Record<string, string> = {`);
for (const e of allEntries) {
  urlLines.push(`  '${e.textureKey}': url_${e.keyid},`);
}
urlLines.push(`};`);
urlLines.push(``);

writeFileSync(OUT_URLS, urlLines.join('\n'), 'utf8');
console.log(`✅ Wrote URL map to ${OUT_URLS}`);
