/**
 * gen-heap-defs.mjs
 *
 * Scans src/sprites/ for PNG sprites, reads their dimensions, and writes:
 *   src/data/heapItemDefs.ts  — the ObjectDef record used by the game
 *   src/data/heapPngUrls.ts   — Vite ?url imports for each sprite PNG
 *
 * Run: node scripts/gen-heap-defs.mjs
 * Re-run after adding or removing sprites.
 */

import sharp from 'sharp';
import { writeFileSync, readdirSync } from 'fs';
import { join, basename, extname, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname     = dirname(fileURLToPath(import.meta.url));
const SPRITES_DIR   = join(__dirname, '..', 'src', 'sprites');
const OUT_DEFS      = join(__dirname, '..', 'src', 'data', 'heapItemDefs.ts');
const OUT_URLS      = join(__dirname, '..', 'src', 'data', 'heapPngUrls.ts');

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

/** Convert a filename (no ext) into a clean slug. */
function slugify(name) {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')   // non-alphanumeric → dash
    .replace(/^-+|-+$/g, '')        // trim leading/trailing dashes
    .replace(/-+/g, '-');           // collapse runs
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

const files = readdirSync(SPRITES_DIR)
  .filter(f => extname(f).toLowerCase() === '.png')
  .sort();

console.log(`Found ${files.length} PNGs in ${SPRITES_DIR}\n`);

const entries = await Promise.all(files.map(async (filename, index) => {
  const pngPath = join(SPRITES_DIR, filename);
  const meta = await sharp(pngPath).metadata();
  const nameNoExt = basename(filename, extname(filename));
  const textureKey = slugify(nameNoExt);
  const width = meta.width;
  const height = meta.height;

  console.log(`  [${String(index).padStart(2)}] ${filename}`);
  console.log(`       size: ${width} × ${height}px`);
  console.log(`       key: "${textureKey}"`);

  return { keyid: index, textureKey, filename, width, height };
}));

// ---------------------------------------------------------------------------
// Emit heapItemDefs.ts
// ---------------------------------------------------------------------------

const lines = [
  `// AUTO-GENERATED by scripts/gen-heap-defs.mjs — do not edit by hand.`,
  `// Re-run the script after adding or removing sprites from src/sprites/.`,
  ``,
  `import { ObjectDef } from './heapTypes';`,
  ``,
  `/**`,
  ` * Maps keyid → object definition.`,
  ` * textureKey must match the key used in BootScene when loading the PNG.`,
  ` * width/height are the actual PNG dimensions in game pixels.`,
  ` */`,
  `export const OBJECT_DEFS: Record<number, ObjectDef> = {`,
];

for (const e of entries) {
  lines.push(
    `  ${e.keyid}: { textureKey: '${e.textureKey}', filename: '${e.filename.replace(/'/g, "\\'")}', width: ${e.width}, height: ${e.height} }, // ${e.filename}`,
  );
}

lines.push(`};`);
lines.push(``);
lines.push(`/** Convenience array ordered by keyid. */`);
lines.push(`export const OBJECT_DEF_LIST = Object.values(OBJECT_DEFS);`);
lines.push(``);
lines.push(`export const HEAP_ITEM_COUNT = ${entries.length};`);
lines.push(``);

writeFileSync(OUT_DEFS, lines.join('\n'), 'utf8');
console.log(`\n✅ Wrote ${entries.length} entries to ${OUT_DEFS}`);

// ---------------------------------------------------------------------------
// Emit heapPngUrls.ts — explicit ?url imports, reliable across all Vite versions
// ---------------------------------------------------------------------------

const urlLines = [
  `// AUTO-GENERATED by scripts/gen-heap-defs.mjs — do not edit by hand.`,
  `// Re-run the script after adding or removing sprites from src/sprites/.`,
  ``,
  `// Vite resolves each ?url import to the correct asset path in dev and production.`,
  `// Explicit per-file imports avoid glob key-normalisation issues.`,
  ``,
];

for (const e of entries) {
  const safeVar = `url_${e.keyid}`;
  urlLines.push(`import ${safeVar} from '../sprites/${e.filename.replace(/'/g, "\\'")}?url';`);
}

urlLines.push(``);
urlLines.push(`/** Maps textureKey → resolved asset URL. Used by BootScene to load sprites. */`);
urlLines.push(`export const HEAP_PNG_URLS: Record<string, string> = {`);
for (const e of entries) {
  urlLines.push(`  '${e.textureKey}': url_${e.keyid},`);
}
urlLines.push(`};`);
urlLines.push(``);

writeFileSync(OUT_URLS, urlLines.join('\n'), 'utf8');
console.log(`✅ Wrote URL map to ${OUT_URLS}`);
